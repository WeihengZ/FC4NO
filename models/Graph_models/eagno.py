class DenseNet(torch.nn.Module):
    def __init__(self, layers, nonlinearity, out_nonlinearity=None, normalize=False):
        super(DenseNet, self).__init__()

        self.n_layers = len(layers) - 1

        assert self.n_layers >= 1

        self.layers = nn.ModuleList()

        for j in range(self.n_layers):
            self.layers.append(nn.Linear(layers[j], layers[j+1]))

            if j != self.n_layers - 1:
                if normalize:
                    self.layers.append(nn.BatchNorm1d(layers[j+1]))

                self.layers.append(nonlinearity)

        if out_nonlinearity is not None:
            self.layers.append(out_nonlinearity)

    def forward(self, x):
        for _, l in enumerate(self.layers):
            x = l(x)

        return x

class EAGNO(torch.nn.Module):
    def __init__(self, args):
        super(EAGNO, self).__init__()

        width_node = args.hidden_dim
        width_kernel = 2 * width_node
        ker_in = 6
        in_channels = args.coor_dim
        out_channels = args.out_dim
        self.act = ACTIVATION[args.act]()
        self.last_act = ACTIVATION[args.last_act]()

        self.fc1 = torch.nn.Linear(in_channels, width_node)

        # cibstruct the GNN kayers
        self.convs = nn.ModuleList()
        for _ in range(args.num_layers):
            kernel = DenseNet([ker_in, width_kernel // 2, width_kernel, width_node**2], self.act)
            conv = NNConv_old(width_node, width_node, kernel, aggr='mean')
            self.convs.append(conv)

        self.fc2 = torch.nn.Linear(width_node, 2 * width_node)
        self.fc3 = torch.nn.Linear(2 * width_node, out_channels)

    def forward(self, data):

        assert hasattr(data, 'edge_index') == True, 'EAGNO need connectivity information'
        assert hasattr(data, 'edge_attr') == True, 'EAGNO need connectivity information'
        assert hasattr(data, 'added_edge_index') == True, 'EAGNO need long-range connectivity information'
        assert hasattr(data, 'added_edge_attr') == True, 'EAGNO need long-range connectivity information'

        x, edge_index, edge_attr = data.x, data.edge_index, data.edge_attr
        
        # add the parameter features in if possible
        if hasattr(data, 'param') == True:
            x = torch.cat((x, data.param.unsqueeze(0).repeat(x.shape[0], 1)), -1)
        x = self.fc1(x)
        
        # combine long-range edges
        edge_index = torch.cat((edge_index, data.added_edge_index), -1)
        edge_attr = torch.cat((edge_attr, data.added_edge_attr), 0)

        # GCN forward
        for conv in self.convs:
            x = conv(x, edge_index, edge_attr)
            x = self.act(x)

        x = self.fc2(x)
        x = F.gelu(x)
        x = self.fc3(x)

        return self.last_act(x)